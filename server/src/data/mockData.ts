function generateTimeseries(): AnalyticsDay[] {
  const days: AnalyticsDay[] = [];
  const now = new Date('2024-06-25');
  for (let i = 29; i >= 0; i--) {
    const d = new Date(now);
    d.setDate(d.getDate() - i);
    const dateStr = d.toISOString().split('T')[0];
    const base = 30 + i;
    const reach = Math.floor(4200000 + Math.random() * 3800000 + Math.sin(i * 0.4) * 1200000);
    const engagement = Math.floor(reach * (0.045 + Math.random() * 0.03));
    const content = Math.floor(120 + Math.random() * 80 + Math.cos(i * 0.3) * 30 + base);
    days.push({ date: dateStr, reach, engagement, content });
  }
  return days;
}

